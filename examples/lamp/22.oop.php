<h2> Object Oriented Programming (OOP) </h2>
<p>Object-oriented programming (OOP) is a programming paradigm that represents concepts as "objects" that have data fields (attributes that describe the object) and associated procedures known as methods. Objects, which are usually instances of classes, are used to interact with one another to design applications and computer programs.</p>

<h3>Definitions</h3>
<ul>
    <li>Class - a template. e.g Dog</li>
    <li>Method - a difined capability of a class. e.g bark()</li>
    <li>Objects or Instance - A particular instance of a class. e.g Lassie</li>
</ul>


 <dl>
    <dt> Class: </dt>
      <dd> Defines the abstract characteristics of a thing (object), including the thing's characteristics (its attributes, fields or properties) and the thing's behaviour (the things it can do, or methods, operations or features). One might say that a class is blueprint or factory that describe the nature of something. For example , the class Dog would consist of traits shared by all dogs, such as breed and fur colour (characteristics), and the ability to bark and sit (behaviours).</dd>

      <dt>Instance:</dt>
      <dd>One can have an instance of a class or a particular object. The instance is the actual object created at runtime. In programmer jargon, the Lassie object is an instance of the Dog class. The set of values of the attributes of a particular object is called its state. The object consists of state and the behaviour that's defined in the object's class. </dd>

      <dt>Method:</dt>
      <dd>An object's abilities. In language, methods are verbs. Lassie, being a Dog, has the ability to bark. So bark() is one of Lassie's methods. She may have other methods as well. For example sit() or eat() or walk() or save_timmy(). Within the program, using a method usually affects onyl one particular object; all Dogs can bark, but you need only one particular dog to do the barking.</dd>
</dl>

<p> <em> Method and Message are often used interchangeably.</em> </p>