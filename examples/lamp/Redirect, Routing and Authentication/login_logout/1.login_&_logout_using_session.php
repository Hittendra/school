<h3>Sesssion / Authentication</h3>
<ul>
    <li>Having a session is not the same as being logged in.</li>
    <li>Generally we have a session the instant we connect to a website.</li>
    <li>The session ID cookie is set when the first page is deliverd.</li>
    <li>Login puts user information in the session (Stored in the server).</li>
    <li>Logout removes user information from the session.</li>
</ul>