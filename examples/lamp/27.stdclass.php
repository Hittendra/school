<pre>
The stdClass is the empty class in PHP which is used to cast other types to object.
 It is similar to Java or Python object. The stdClass is not the base class of the objects. 
 If an object is converted to object, it is not modified. 
 But, if object type is converted/type-casted an instance of stdClass is created, if it is not NULL. 
 If it is NULL, the new instance will be empty.

Uses:

The stdClass directly access the members by calling them.
It is useful in dynamic object.
It is used to set dynamic properties etc.
